import { ConnectButton } from '@rainbow-me/rainbowkit';
import type { NextPage } from 'next';
import Head from 'next/head';
import styles from '../styles/Home.module.css';
import ABI from "../contract/contract.json";
import { useContractRead, useContractWrite, usePrepareContractWrite } from 'wagmi'
import { useEffect, useState } from "react";

const contractAddress = '0x1FBF91BcB3e53d75CE736B393368eE627eE1dc30';

const Home: NextPage = () => {
  //const { ensData } = useEnsData(undefined);
  const [store, setStore] = useState(false);
  const [save, setSave] = useState(false);
  const [userInput, setUserInput] = useState('');
  const { data: SwitchData, isError, isLoading } = useContractRead({
    address: contractAddress,
    abi: ABI,
    functionName: 'retrieve',
    onSuccess(data) {
      setStore(data)
    }
  })
  const { data: nameData, refetch } = useContractRead({
    address: contractAddress,
    abi: ABI,
    functionName: 'viewName',
    staleTime: 500,
    onSuccess(data) {
      //setStore(data)
      console.log('viewName data, ', nameData);
    }
  })
  const { config: contractMintWriteConfig } = usePrepareContractWrite({
    address: contractAddress,
    abi: [
      {
        name: 'store',
        type: 'function',
        stateMutability: 'nonpayable',
        inputs: [{ internalType: 'bool', name: '_v', type: 'bool' }],
        outputs: [],
      },
    ],
    functionName: 'store',
    args: [store],
  });
  const { config: setNameConfig } = usePrepareContractWrite({
    address: contractAddress,
    abi: ABI,
    functionName: 'setName',
    args: [userInput],
  });
  const {
    write: writeName,
    // isLoading: isStoreLoading,
    // isSuccess: isStoreStarted,
    // error: StoreError,
  } = useContractWrite(setNameConfig);
  useEffect(() => {
    if (save) {
      storeWrite?.()
      setSave(false)
    }
  }, [store]);
  const {
    write: storeWrite,
    isLoading: isStoreLoading,
    isSuccess: isStoreStarted,
    error: StoreError,
  } = useContractWrite(contractMintWriteConfig);

  const buttonHandler = () => {
    console.log('user input ', userInput);
    if (userInput) {
      writeName?.();
    }
    setUserInput('');
  }
  const onInputChange = (e) => {
    setUserInput(e.target.value);
  }
  const fetchHandler = () => {
    refetch();
  }
  return (
    <div className={styles.container}>
      <Head>
        <title>RainbowKit App</title>
        <meta
          name="description"
          content="Generated by @rainbow-me/create-rainbowkit"
        />
        <link rel="icon" href="/favicon.ico" />
      </Head>

      <main className={styles.main}>
        <ConnectButton />
        <div>
          <input type="radio" id="huey" name="drone" value="true"
            checked={store === true}
            onChange={(e) => {
              setStore(true)
              setSave(true)
            }}
          />
          <label>true</label>
        </div>

        <div>
          <input type="radio" id="dewey" name="drone" value="false"
            checked={store === false}
            onChange={(e) => {
              setStore(false)
              setSave(true)
            }}
          />
          <label>false</label>
        </div>
        <div style={{display: 'flex', justifyContent: 'space-between', width: '350px', marginTop: '5px'}}>
          <div>Name: {nameData}</div>
          <button onClick={fetchHandler}>Click to fetch name</button>
        </div>
        <div style={{display: 'flex', justifyContent: 'space-between', width: '350px', marginTop: '5px'}}>
            <input placeHolder="type new name" value={userInput} onChange={onInputChange}/>
            <button onClick={buttonHandler}>Click to set new name</button>
        </div>
      </main>

      <footer className={styles.footer}>
        <a href="https://rainbow.me" target="_blank" rel="noopener noreferrer">
          Made with ❤️ by your frens at 🌈
        </a>
      </footer>
    </div>
  );
};

export default Home;
